# 중첩된 형태의 리스트
# 리스트 내부에 또다른 리스트가 포함된 형태
list_1 = [1, 2, 3, ['a','b','c']]

print(f"list_1[0] -> {list_1[0]}")
print(f"list_1[1] -> {list_1[1]}")
print(f"list_1[2] -> {list_1[2]}")

print(f"list_1[3][0] -> {list_1[3][0]}")
print(f"list_1[3][1] -> {list_1[3][1]}")
print(f"list_1[3][2] -> {list_1[3][2]}")





