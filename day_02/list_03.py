# 문자열 데이터는 배열(리스트) 형태로 저장되며,
# 값의 수정이 허용되지 않는 데이터입니다.
msg = "Hello World~!"
print(msg)

# 문자열 데이터를 수정이 불가능합니다.
#msg[-2] = "!"
#print(msg)

# 반면, 리스트 타입으로 선언된 데이터는
# 각 요소의 수정이 가능합니다.
numbers = [1,2,3,4,5]
print(numbers)
numbers[2] = 30
print(numbers)








