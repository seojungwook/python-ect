# 함수(Function)
# 기능을 정의하는 단위
# 코드의 재활용을 위해서 사용되는 문법
# 프로그램을 개발하는 도중 반복되서 사용되는 코드의 묶음을
# 함수로 정의하여 재활용할 수 있음

# def 함수명( [매개변수] ) :
#   함수의 호출 시, 실행할 실행문...
def hello() : 
    print("hello 함수 실행~!")
    
# 함수로 정의된 실행코드는 호출되기 전까지 실행되지
# 않습니다.
    
# 함수의 내용을 실행하기 위해서는 해당 함수를 
# 호출해야 합니다.    
# 함수의 호출 방법
# 함수명([해당 함수의 실행에 필요한 매개변수])
hello()

# 함수는 선언된 이후에 계속해서 호출할 수 있습니다.
hello()    













