# 파이썬의 모든 데이터는 타입을 가집니다.
# 정수(int), 실수(float), 진리형(bool), 문자열(str)

# type 함수
# 값 또는 변수의 타입을 반환하는 함수
print("{0} -> {1}".format(10, type(10)))
print("{0} -> {1}".format(10.7, type(10.7)))
print("{0} -> {1}".format(True, type(True)))
print("{0} -> {1}".format("Hello", type("Hello")))










